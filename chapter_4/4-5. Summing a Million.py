# this will give you a list with the numbers from 1 to 1000000.
number_list = list(range(1, 1000001))
min_number = min(number_list)
max_number = max(number_list)
sum_numbers = sum(number_list)
print(f"the min number:{min_number} , the max number:{max_number}, the sum of the numbers:{sum_numbers}")
